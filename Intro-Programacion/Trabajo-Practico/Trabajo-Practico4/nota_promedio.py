
nombre = input("Ingrese apellido: ")
primer_parcial = float(input("Ingrese nota de primer parcial: "))
segundo_parcial = float(input("Ingrese nota de segundo parcial: "))
promedio = (primer_parcial + segundo_parcial) / 2

print(f"Alumno: {nombre}")
print(f"Primer parcial: {primer_parcial}")
print(f"Segundo parcial: {segundo_parcial}")
print(f"Promedio: {promedio}")